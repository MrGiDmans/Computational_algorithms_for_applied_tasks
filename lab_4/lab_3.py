import numpy as np

# Количество переменных = 20
# Порядок переменных:
# [x12, x13, x14, x15, x21, x23, x24, x25,
#  x31, x32, x34, x35, x41, x42, x43, x45,
#  x51, x52, x53, x54]

A = np.zeros((5, 20))

# Уравнение 1: x21+x31+x41+x51 - (x12+x13+x14+x15)=0
A[0,4] = 1   # x21
A[0,8] = 1   # x31
A[0,12] = 1  # x41
A[0,16] = 1  # x51
A[0,0] = -1  # x12
A[0,1] = -1  # x13
A[0,2] = -1  # x14
A[0,3] = -1  # x15

# Уравнение 2
A[1,0] = 1   # x12
A[1,9] = 1   # x32
A[1,13] = 1  # x42
A[1,17] = 1  # x52
A[1,4] = -1  # x21
A[1,5] = -1  # x23
A[1,6] = -1  # x24
A[1,7] = -1  # x25

# Уравнение 3
A[2,1] = 1   # x13
A[2,5] = 1   # x23
A[2,14] = 1  # x43
A[2,18] = 1  # x53
A[2,8] = -1  # x31
A[2,9] = -1  # x32
A[2,10] = -1 # x34
A[2,11] = -1 # x35

# Уравнение 4
A[3,2] = 1   # x14
A[3,6] = 1   # x24
A[3,10] = 1  # x34
A[3,19] = 1  # x54
A[3,12] = -1 # x41
A[3,13] = -1 # x42
A[3,14] = -1 # x43
A[3,15] = -1 # x45

# Уравнение 5
A[4,3] = 1   # x15
A[4,7] = 1   # x25
A[4,11] = 1  # x35
A[4,15] = 1  # x45
A[4,16] = -1 # x51
A[4,17] = -1 # x52
A[4,18] = -1 # x53
A[4,19] = -1 # x54

# Теперь решаем систему A @ x = 0
# Находим базис пространства решений (ядра матрицы)
U, S, Vt = np.linalg.svd(A)
rank = np.sum(S > 1e-10)
null_space = Vt[rank:].T

print("Ранг матрицы:", rank)
print("Размерность пространства решений:", null_space.shape[1])
print("Базис пространства решений:")
print(null_space)
